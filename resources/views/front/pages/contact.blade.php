@section('customJs')
    <script>
        $(document).on('submit', '#add_contact', function(event) {
            event.preventDefault();
            $.ajax({
                url: "{{ route('front_add_contact') }}",
                method: 'POST',
                data: new FormData(this),
                dataType: 'JSON',
                contentType: false,
                processData: false,
                success: function(data) {
                    webinaToast({
                        type: data.type,
                        message: data.text
                    });
                    if (data.type == 'success') {
                        $('#add_contact')[0].reset();
                    }
                }
            });
        });
    </script>
